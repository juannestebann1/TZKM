function Oraculo(){

    this.maya = {
        'umbral':{kin:0,sello:1,tono:1},
        'analogo':{kin:0,sello:1,tono:1},
        'antipoda':{kin:0,sello:1,tono:1},
        'oculto':{kin:0,sello:1,tono:1},
        'guia':{kin:0,sello:1,tono:1},
        'ano':{kin:0,sello:1,tono:1}
    };

    this.kinUmbral;

    //return this.maya;
}

Oraculo.prototype.Datekin = function(dia, mes, ano) {

    let a = parseInt(ano);
    let d = parseInt(dia)
    let m = parseInt(mes)

    let kinrev = d+YEAR[a]+MESES[m];

    if (kinrev > 260) { kinrev = kinrev - 260 };

    if (kinrev > 260) { kinrev = kinrev - 260 };

    if (kinrev > 260) { kinrev = kinrev - 260 };

    this.kinUmbral = kinrev;
};

Oraculo.prototype.tzolkin = function(d={}) {
    let u;

    if (typeof d.dia != 'undefined' && typeof d.mes != 'undefined' && typeof d.ano != 'undefined') {
        this.Datekin(d.dia, d.mes, d.ano)
    }

    if (typeof d.kin != 'undefined') {

        this.kinUmbral = d.kin
    }else{
        this.kinUmbral = this.kinUmbral;
    }


    if (typeof d.umbral === 'undefined') {
        u = "umbral";
    }else{
        u = d.umbral;
    }

    let matriz = Matriz[this.kinUmbral];

    this.maya[u].kin    =  matriz.kin;
    this.maya[u].tono   =  TONO[matriz.tono];
    this.maya[u].sello  =  SELLO[matriz.sello];

    if (d.run) {
       //Code
    }else{
        this.init();
        x = function (a) {
            let C = 0;
            let n = 0;
            a = a/4;
            if (a === 0.25) {
                C = 1;
            }else if(a === 0.5){
                C = 2;
            }else if(a === 0.75){
                C = 3;
            }else{
                C = a
            }
            t = a.toString();
            x = t.split('.');
            if (x[1] != undefined) {
                n = parseInt(x[0])+1;
            }else{}
            return n;
        }
        this.maya.umbral.celula = CELULA[x(this.maya.umbral.kin)];
        if (typeof d.ano != 'undefined') {
            this.Ano(YEAR[d.ano])
        }
    }
}

Oraculo.prototype.Ano = function (a) {
    a = a+102
    if (a > 260) {
        a = a-260;
    }else{
        a = a
    }
    this.tzolkin({'kin':a, 'umbral':'ano', 'run':false});
}

Oraculo.prototype.Onda = function () {
    k = this.maya.umbral.sello.Codigo+1
    for (var i = this.maya.umbral.tono.Codigo; i >= 1; i--) {
        if (k === 0) {
            k= k+20
        }
        k--
    }
    this.maya.umbral.onda = k;
}

Oraculo.prototype.Castillo = function () {
    if (this.maya.umbral.kin >=1 && this.maya.umbral.kin <= 52) {
        this.maya.umbral.castillo = {
            'color':'Rojo',
            'ubicacion':'Este del girar',
            'c':'#A60E0C'
        }
    }else if (this.maya.umbral.kin >= 53 && this.maya.umbral.kin <=104) {
        this.maya.umbral.castillo = {
            'color':'Blanco',
            'ubicacion':'Norte del cruzar',
            'c':'white'
        }
    }
    else if (this.maya.umbral.kin >= 105 && this.maya.umbral.kin <=156) {
        this.maya.umbral.castillo = {
            'color':'Azul',
            'ubicacion':'Oeste del quemar',
            'c':'blue'
        }
    }
    else if (this.maya.umbral.kin >= 157 && this.maya.umbral.kin <= 208) {
        this.maya.umbral.castillo = {
            'color':'Amarillo',
            'ubicacion':'Sur del dar',
            'c':'yellow'
        }
    }else if (this.maya.umbral.kin >= 209 && this.maya.umbral.kin <=260) {
        this.maya.umbral.castillo = {
            'color':'Verde',
            'ubicacion':'Central del encantar',
            'c':'green'
        }
    }
}

Oraculo.prototype.Guia = function () {

    let GuiaSello;

    if(this.maya.umbral.tono.Codigo === 1 || this.maya.umbral.tono.Codigo === 6 || this.maya.umbral.tono.Codigo === 11) {

        GuiaSello = this.maya.umbral.sello.Codigo
    }else if (this.maya.umbral.tono.Codigo === 2 || this.maya.umbral.tono.Codigo === 7 || this.maya.umbral.tono.Codigo === 12) {

        GuiaSello = this.maya.umbral.sello.Codigo-8
        if (GuiaSello < 0) {
            GuiaSello = GuiaSello+20;
        }
    }else if (this.maya.umbral.tono.Codigo === 3 || this.maya.umbral.tono.Codigo === 8 || this.maya.umbral.tono.Codigo === 13) {

        GuiaSello = this.maya.umbral.sello.Codigo+4
        if (GuiaSello > 20) {
            GuiaSello = GuiaSello-20;
        }
    }else if (this.maya.umbral.tono.Codigo === 4 || this.maya.umbral.tono.Codigo === 9) {

        GuiaSello = this.maya.umbral.sello.Codigo-4
        if (GuiaSello < 0) {
            GuiaSello = GuiaSello+20;
        }
    }else if (this.maya.umbral.tono.Codigo === 5 || this.maya.umbral.tono.Codigo === 10) {

        GuiaSello = this.maya.umbral.sello.Codigo+8
        if (GuiaSello > 20) {
            GuiaSello = GuiaSello-20;
        }
    }

    let GuiaTono = this.maya.umbral.tono.Codigo;
    let Guia = Matriz.find( kin => kin.sello === GuiaSello && kin.tono === GuiaTono);
    this.tzolkin({'kin':Guia.kin, 'umbral':'guia', 'run':true});
}

Oraculo.prototype.Oculto = function () {

    let OcultoSello = 21-this.maya.umbral.sello.Codigo;
    let OcultoTono = 14-this.maya.umbral.tono.Codigo;
    let Oculto = Matriz.find( kin => kin.sello === OcultoSello && kin.tono === OcultoTono);
    this.tzolkin({'kin':Oculto.kin, 'umbral':'oculto', 'run':true});
}

Oraculo.prototype.Analogo = function () {

    let AnalogoSello = 19-this.maya.umbral.sello.Codigo;
    let AnalogoTono = this.maya.umbral.tono.Codigo;
    let Analogo = Matriz.find( kin => kin.sello === AnalogoSello && kin.tono === AnalogoTono);
    this.tzolkin({'kin':Analogo.kin, 'umbral':'analogo', 'run':true});
}

Oraculo.prototype.Antipoda = function () {

    let AntipodaSello;

    if ((this.maya.umbral.sello.Codigo+10) > 20) {

        AntipodaSello = (this.maya.umbral.sello.Codigo+10)-20
    }else{

        AntipodaSello = this.maya.umbral.sello.Codigo+10
    }

    let AntipodaTono = this.maya.umbral.tono.Codigo;
    let Antipoda = Matriz.find( kin => kin.sello === AntipodaSello && kin.tono === AntipodaTono);
    this.tzolkin({'kin':Antipoda.kin, 'umbral':'antipoda', 'run':true});
}

Oraculo.prototype.init = function () {
    this.Guia();
    this.Antipoda();
    this.Analogo();
    this.Oculto();
    this.Castillo();
    this.Onda();
}

